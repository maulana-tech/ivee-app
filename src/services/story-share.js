import { toFlagEmoji } from '@/utils/country-flag';
import { getCanonicalApiOrigin } from '@/services/runtime';
const VALID_STORY_TYPES = ['ciianalysis', 'convergence', 'brief'];
function sanitizeCountryCode(value) {
    const code = String(value || '').trim().toUpperCase();
    return /^[A-Z]{2}$/.test(code) ? code : '';
}
function sanitizeStoryType(value) {
    const t = String(value || '').trim().toLowerCase();
    return VALID_STORY_TYPES.includes(t) ? t : 'ciianalysis';
}
// Deep link generator for story sharing
export function generateStoryDeepLink(countryCode, type = 'ciianalysis', score, level) {
    const safeCountryCode = sanitizeCountryCode(countryCode);
    const safeType = sanitizeStoryType(type);
    const params = new URLSearchParams({
        c: safeCountryCode,
        t: safeType,
        ts: Date.now().toString()
    });
    if (score !== undefined)
        params.set('s', String(score));
    if (level)
        params.set('l', String(level).trim().slice(0, 32));
    return `${getCanonicalApiOrigin()}/api/story?${params.toString()}`;
}
// Parse deep link parameters
export function parseStoryParams(url) {
    const countryCode = url.searchParams.get('c');
    if (!countryCode)
        return null;
    const safeCountryCode = sanitizeCountryCode(countryCode);
    if (!safeCountryCode)
        return null;
    return {
        countryCode: safeCountryCode,
        type: sanitizeStoryType(url.searchParams.get('t'))
    };
}
// Generate QR code data URL (simple implementation)
export function generateQRCode(data, size = 200) {
    // Using a simple QR code library pattern
    // In production, use a library like qrcode or node-qrcode
    const canvas = document.createElement('canvas');
    canvas.width = size;
    canvas.height = size;
    const ctx = canvas.getContext('2d');
    // Placeholder - would use actual QR library
    ctx.fillStyle = '#ffffff';
    ctx.fillRect(0, 0, size, size);
    ctx.fillStyle = '#000000';
    ctx.font = '14px monospace';
    ctx.textAlign = 'center';
    ctx.fillText('Scan to view', size / 2, size / 2 - 10);
    ctx.fillText(data.substring(0, 20) + '...', size / 2, size / 2 + 10);
    return canvas.toDataURL('image/png');
}
